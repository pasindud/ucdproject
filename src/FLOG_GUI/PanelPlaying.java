    public void drawOpponents(PlayerData[] pdArr)
    {
        pnlOppList.setLayout(new BoxLayout(pnlOppList, BoxLayout.Y_AXIS));
        pnlOppList.setOpaque(false);
        
        for(int i=0;i<pdArr.length;i++)
        {
            
            pnlOppList.add(new OpponentsUI(pdArr[i].getPosition(), pdArr[i].getName(), pdArr[i].getScore(), pdArr[i].getLetterOne(), pdArr[i].letterTwo));
        }
        
        jsp = new JScrollPane(pnlOppList);
        jsp.setOpaque(false);
        jsp.setBorder(null);
        jsp.getViewport().setOpaque(false);
        jsp.getVerticalScrollBar().setPreferredSize(new Dimension(0,0));
        jPanel3.add(jsp).setBounds(1, 20, 230, 510);
        
    }
    
